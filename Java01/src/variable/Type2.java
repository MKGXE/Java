package variable;

public class Type2 {
	public static void main(String[] args) {
		// 변수를 활용해서
		// 이름, 출생년도, 키
		
		// 예시의 000을 변수로 활용
		/*
		 이름 : 000
		 출생년도 : 0000년
		 키 : 000.00cm
		 */
		
		String name = "최민혁";
		int year = 2007;
		double height = 177.5;
		
		System.out.println("이름 : "+name);
		System.out.println("출생년도 : "+year+"년");
		System.out.println("키 : "+height+"cm");
	}
}
